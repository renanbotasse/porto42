#include "ft_printf.h"

int	ft_puts_n(char *s, int n)
{
	int	c;

	c = 0;
	if (!s)
		return (write(n, "NULL", 6));
	
    while (s[c] != '\0')
	{
		ft_putchar_n(s[c], n);
		c++;
	}
	return (c);
}