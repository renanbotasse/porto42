#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	unsigned int	i;
	size_t			len;
	char			*str;

	len = ft_strlen(src);
	str = (char *)malloc(sizeof(*str) * len + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
