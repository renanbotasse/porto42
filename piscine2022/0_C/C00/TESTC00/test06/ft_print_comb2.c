#include <unistd.h>

void	ft_print_comb2(void)
{
	int	p;
	int	s;
	char	c;

	p = -1;
	while (++p <= 98)
	{
		s = p;
		while (++s <= 99)
		{
			c = (p / 10) + '0';
			write(1, &c, 1);
			c = (p % 10) + '0';
			write(1, &c, 1);
			c = ' ';
			write(1, &c, 1);
			c = (s / 10) + '0';
			write(1, &c, 1);
			c = (s % 10) + '0';
			write(1, &c, 1);
			if ((p != 98))
			{
				c = ',';
				write(1, &c, 1);
				c = ' ';
				write(1, &c, 1);
			}
		}
	}
}


int main()
{
  ft_print_comb2();
  return(0);
}
